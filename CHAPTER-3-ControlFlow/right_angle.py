
hypotenuse = float(input('Enter the length of  hypotenuse:'))
adjacent = float(input('Enter the length of  adjacent:'))
base = float(input('Enter the length of  base:'))


if  (hypotenuse ** 2  == (adjacent ** 2 + base ** 2)):
    print('This is a right angled triangle')
else:
    print('This is not a right angled triangle')
    

